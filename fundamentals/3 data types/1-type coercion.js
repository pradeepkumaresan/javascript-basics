const value1 = '10';
const value2 = 20;

let sum = value1 + value2;

console.log(sum); // 1020

// this is an example of type coercion in JavaScript. 
// The value of value1 is a string, and the value of value2 is a number. 
// When we add them together, JavaScript coerces the number to a string 
// and concatenates the two values. This is why the result is '1020' 
// instead of 30.